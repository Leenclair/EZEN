package day12;

public class Calculator {
	public double plus(double a, double b) {
		double c = a+b;
		return c;
	}
	public double minus(double a, double b) {
		double c = a-b;
		return c;
	}
	public double multiply(double a, double b) {
		double c = a*b;
		return c;
	}
	public double divide(double a, double b) {
		double c = a/b;
		return c;
	}
}
